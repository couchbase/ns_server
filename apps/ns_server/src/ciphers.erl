is_valid_name(Bin) when is_binary(Bin) ->
    maps:is_key(Bin, all()).

code(Name) ->
    case maps:get(Name, all(), undefined) of
        undefined -> undefined;
        #cipher{code = Bytes} -> Bytes
    end.

openssl_name(Name) ->
    maps:get(code(Name), openssl_ciphers(), undefined).

